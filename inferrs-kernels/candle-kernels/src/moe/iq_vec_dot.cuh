// ────────────────────────────────────────────────────────────
// Block struct definitions
// ────────────────────────────────────────────────────────────

typedef struct {
    half     d;
    uint16_t qs[QK_K/8];   // 9-bit indices into iq2xs_grid (lower 9 bits) + 7-bit signs (upper 7 bits)
    uint8_t  scales[QK_K/32];
} block_iq2_xs;
static_assert(sizeof(block_iq2_xs) == sizeof(half) + QK_K/8*sizeof(uint16_t) + QK_K/32,
              "wrong iq2_xs block size/padding");

typedef struct {
    half    d;
    uint8_t qs[3*(QK_K/8)]; // first QK_K/4 bytes: 3-bit quants; next QK_K/8 bytes: aux (scale + signs)
} block_iq3_xxs;
static_assert(sizeof(block_iq3_xxs) == sizeof(half) + 3*(QK_K/8),
              "wrong iq3_xxs block size/padding");

